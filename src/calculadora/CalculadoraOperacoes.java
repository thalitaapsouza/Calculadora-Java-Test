package calculadora;

public class CalculadoraOperacoes {

    public int somar(int a, int b) {
        return a + b;
    }

    public int subtrair(int a, int b) {
        return a - b;
    }
    public int multiplicacao(int a, int b) {
        return a * b;
    }
    public double divisao(double a, double b) {
        return a / b;
    }
}
